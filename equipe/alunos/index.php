				<p>Os estudantes-pesquisadores que fazem parte do grupo de Nanoneurobiofísica (GNN) estão vinculados à cursos de graduação em Biologia, Física, Farmácia, Medicina, Química, Engenharias e Tecnológicos. Todos os alunos são estudantes de graduação ou pós-graduação, além de pós-doutores e pesquisadores visitantes. Nossos membros são fortemente encorajados a interagir com áreas multidisciplinares e de fronteira. Esse perfil permite-os interpretar resultados e tendências de problemas complexos com maior grau de sucesso.</p>
				<p>Devido o caráter interdisciplinar do grupo, os estudantes de iniciação científica possuem um perfil bastante peculiar, com alto grau de independência e maturidade, e com uma excelente performance acadêmica, além da forte perspectiva de cursar uma pós-graduação. O perfil transdisciplinar e ousado do GNN permite criar uma equipe coesa, disciplinada, dinâmica e com novas perspectivas para o mercado de trabalho. Ressalta-se que todos os alunos são encorajados a participar da Escola Preparatória para Futuros Cientistas, organizada no verão de todos os anos, pelo Programa Futuro Cientista® (PFC). Os alunos de mestrado e doutorado podem realizar suas pesquisas em qualquer centro ou universidade filiada ao GNN, desde que possuam a aprovação de um Coordenador de Área, representante do grupo. </p>
				<p>Os alunos de graduação e pós-graduação do GNN devem possuir os seguintes requisitos: </p>
				<ul>
					<li>Proficiência em inglês,</li>
					<li>Perfil de cientista (<a href="http://www.futurocientista.net" target="_blank">www.futurocientista.net</a>)</li>
					<li>Forte perspectiva de continuidade (mestrado, doutorado, pós-doutorado, jovem pesquisador, etc).</li>
