				<p>Nanoneurobiofísica é a aplicação dos princípios fundamentais da Física e da Nanociência no desenvolvimento de novos materiais e dispositivos para o estudo de doenças neurodegenerativas, doenças tropicais e contaminantes ambientais. </p>
