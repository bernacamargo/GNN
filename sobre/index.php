				<p>Esta nova área de pesquisa engloba as áreas fundamentais da ciência como a Física, a Biologia, a Neurociência e a Nanociência/Nanotecnologia, formando o eixo formador do Grupo de Pesquisa em NanoNeuroBioFísica. As áreas transversais atuam como um eixo unificador, em torno do qual se organizam as Linhas de Pesquisa, devendo ser trabalhadas de modo coordenado e não desconectadas. O papel desta nova grande área de pesquisa é contextualizar as áreas transversais e o eixo principal e facilitar, fomentar e integrar as ações de modo contextualizado, através da interdisciplinaridade e transversalidade, buscando não fragmentar o conhecimento em blocos rígidos, permitindo uma interação entre as diversas linhas de pesquisa. As interações que ocorrem entre áreas básicas da ciência criam uma sinergia e um processo de pesquisa transdisciplinar criando diversas linhas de pesquisa como a Nanoneurociência, Neurobiologia, Biofísica, Nanobiologia, Neurofísica e Nanofísica. As áreas transversais da rede são a Engenharia, a Geografia, a Informática, a Medicina, A Química e Tecnologia. </p>
