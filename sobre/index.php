				<p>O grupo de nanoneurobiofísica (GNN) está vinculado a várias instituições renomadas e de abrangência internacional, com enfoque transdisciplinar, dedicado ao estudo de temas vanguardistas na interface da nanociência/nanotecnologia, neurociência, física, biologia, química e engenharia de materiais. A missão do grupo é estabelecer colaborações multi e transdiciplinares entre físicos, químicos, biólogos, neurocientistas e cientistas teóricos para desenvolver produtos, materiais e novas teorias no estudo de doenças tropicais e/ou neurodegenerativas. </p>
